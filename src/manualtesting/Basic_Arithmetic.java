    private void button_divActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_divActionPerformed
        // TODO add your handling code here
        String first_ip = acceptInput(first_num);
        String second_ip = acceptInput(second_num);

        if (isFloat(first_ip) && isFloat(second_ip)) {
            if (Float.parseFloat(second_ip) != 0) {
                float resultval = Float.parseFloat(first_ip) / Float.parseFloat(second_ip);
                String output = String.format("%.3f", resultval);
                result.setText(output);
            } else {
                JOptionPane.showMessageDialog(null, "Integer divided by 0 is undefined.", "Note", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Enter valid input!", "Warning!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_button_divActionPerformed

