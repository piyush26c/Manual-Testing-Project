    public static boolean isFloat(String s) {
        try {
            Float.parseFloat(s);
        } catch (NumberFormatException e) {
            return false;
        }
        // only got here if we didn't return false
        return true;
    }

    public String acceptInput(JTextField field) {
        String ip = field.getText().replaceAll("\\s", "");
        return ip;
    }

    public String addNumbers(String num1, String num2) {
        float resultval = Float.parseFloat(num1) + Float.parseFloat(num2);
        String output = String.format("%.3f", resultval);
        return output;
    }


    private void button_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_addActionPerformed
        // TODO add your handling code here:
        String first_ip = acceptInput(first_num);
        String second_ip = acceptInput(second_num);

        if (isFloat(first_ip) && isFloat(second_ip)) {
            String output = addNumbers(first_ip, second_ip);
            result.setText(output);
        } else {
            JOptionPane.showMessageDialog(null, "Enter valid input!", "Warning!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_button_addActionPerformed

